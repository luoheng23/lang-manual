// Int
// swift语言提供各种大小的整型
// swift提供128-bit的整型
// int8 -> Int8
// uint8 -> UInt8
// int16 -> Int16
// uint16 -> UInt16
// int32 -> Int32
// uint32 -> UInt32
// int64 -> Int64
// uint64 -> UInt64
// int128 -> Int128
// uint128 -> UInt128
// int -> Int
// uint -> UInt

let max_long_long: Int64 = Int64.max
let min_long_long: Int64 = Int64.min
let max_int: Int = Int.max
let min_int: Int = Int.min

print("int64: ", "min: ", min_long_long, "max: ", max_long_long)
print("int: ", "min: ", min_int, "max: ", max_int)
